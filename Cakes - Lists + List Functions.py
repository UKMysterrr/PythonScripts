#List Created Of 5 Cakes

cakes = ["Victoria Sponge","Wedding","Chocolate","Carrot","Birthday"]

#Prints The List Of Cakes

print(cakes)

#Prints The First Cake, Victoria Sponge

print(cakes[0])

#Prints The First 3 Items

print(cakes[0:4])

#Adds "Banana" To The List

cakes.append("Banana")

#Prints The Full List

print(cakes)

#Sorts And Prints The List

cakes.sort()
print(cakes)

#Removes The Last Object On The List

cakes.pop()

#Changes "Wedding" to "Christmas"

cakes[4] = "Christmas"

#Prints The Final List

print(cakes)
